class Scooter{
    constructor(yr, clrstr, mdlstr)
    {
        this.year = yr;
        this.color = clrstr;
        this.model = mdlstr;
    }
}

class Driver{
    constructor(nm, ag, exprc)
    {
        this.name = nm;
        this.age = ag;
        this.experience = exprc;
    }
}

class PickupLocation{
    constructor(addr, cty)
    {
        this.address = addr;
        this.city = cty;
    }
}
